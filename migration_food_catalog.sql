-- 1. ТАБЛИЦА
CREATE TABLE IF NOT EXISTS food_products (
    id               SERIAL PRIMARY KEY,
    name             VARCHAR(200) NOT NULL,
    name_en          VARCHAR(200),
    calories         FLOAT NOT NULL,
    protein          FLOAT NOT NULL,
    fat              FLOAT NOT NULL,
    carbs            FLOAT NOT NULL,
    fiber            FLOAT DEFAULT 0,
    category         VARCHAR(50) NOT NULL,
    image_url        TEXT,
    is_custom        BOOLEAN DEFAULT FALSE,
    created_by_user_id BIGINT,
    created_at       TIMESTAMP DEFAULT NOW(),
    is_verified      BOOLEAN DEFAULT TRUE,
    -- Поля только для добавок (NULL для обычных продуктов)
    serving_size_g   FLOAT,
    servings_per_pack INT,
    brand            VARCHAR(100),
    supplement_type  VARCHAR(50),
    search_vector    TSVECTOR
);

CREATE INDEX IF NOT EXISTS idx_food_products_search   ON food_products USING gin(search_vector);
CREATE INDEX IF NOT EXISTS idx_food_products_category ON food_products(category);
CREATE INDEX IF NOT EXISTS idx_food_products_custom   ON food_products(is_custom);
CREATE INDEX IF NOT EXISTS idx_food_products_brand    ON food_products(brand);

CREATE OR REPLACE FUNCTION update_food_search_vector()
RETURNS TRIGGER AS $$
BEGIN
    NEW.search_vector :=
        to_tsvector('russian', COALESCE(NEW.name, '')) ||
        to_tsvector('english', COALESCE(NEW.name_en, ''));
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS food_products_search_vector_update ON food_products;
CREATE TRIGGER food_products_search_vector_update
    BEFORE INSERT OR UPDATE ON food_products
    FOR EACH ROW EXECUTE FUNCTION update_food_search_vector();

